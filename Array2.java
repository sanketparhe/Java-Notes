import java.util.*;
public class Array2 {
    public static void main(String[] args) {
      Scanner sc=new Scanner(System.in);
      System.out.println("Enter Value");
      int a=sc.nextInt();
      int arr[]= new int[a];
      for(int i=0; i<arr.length; i++){
        System.out.println(arr[i]);
      }
    }
}
